    private void clearAccountValueTable(){
        int rowCount = accountValuesTable.getRowCount();
        int colCount = accountValuesTable.getColumnCount();
        for (int il = 0; il < rowCount; il++){
            for (int ic = 0; ic < colCount; ic++) {
                accountValuesTable.getModel().setValueAt("", il, ic);
            }
        }
    }
    private class DisplayAccountValues extends Thread {
        String position = "";
        String result = "";
        
        DisplayAccountValues() {            
            displayValuesRunning = true;
            this.start();
        }
        @Override
        public void run() {
            PosValues actPosVal = new PosValues();
            System.out.println("\ndisplayAccountValues Running");
            boolean isThisAnOption = false;
            clearAccountValueTable();
            while (displayValuesRunning == true){                
                actAccInfo = actIbApi.new accountInfo(userSelectedAccount.substring(0, userSelectedAccount.indexOf("(")));
                actIbApi.setActAccountInfo(actAccInfo);
                actAccInfo.reqUpdateAccountInformation(true);
                myUtils.delay(2000);
                actAccInfo.reqUpdateAccountInformation(false);
                System.out.println("\nportfolioSize = " + actAccInfo.getPortfolioListSize());
                listOfPositions.clear();
                posValueList.clear();
                calcPosValues1();
                accountValuesTable.clearSelection();
                for (int idp = 0; idp < posValueList.size(); idp++) {  
                    actPosVal = posValueList.get(idp);
                    isThisAnOption = (actPosVal.secType.equals("OPT") ? true : false);
                    qInfo = actChain.getQuote(
                            (actPosVal.secType.equals("OPT") ? actPosVal.optionSymbol : actPosVal.symbol), 
                            isThisAnOption
                    );
                    accountValuesTable.getModel().setValueAt(
                            (actPosVal.secType.equals("OPT") ? actPosVal.optionName : actPosVal.symbol)
                            , idp, AccValTable.oContract.ordinal()
                    );
                    accountValuesTable.getModel().setValueAt(myUtils.roundMe(qInfo.last, 2), idp, AccValTable.oLastQ.ordinal());
                    accountValuesTable.getModel().setValueAt(actPosVal.numOfShares, idp, AccValTable.oShares.ordinal());
                    accountValuesTable.getModel().setValueAt(actPosVal.marketVal, idp, AccValTable.oCurrentValue.ordinal());
                    accountValuesTable.getModel().setValueAt(actPosVal.aveCost, idp, AccValTable.oCost.ordinal());
                    accountValuesTable.getModel().setValueAt(actPosVal.unrealizedPNL, idp, AccValTable.oProfit.ordinal());
                    accountValuesTable.getModel().setValueAt(actPosVal.maxPosGain, idp, AccValTable.oMaxGain.ordinal());
                    accountValuesTable.getModel().setValueAt(actPosVal.curGainLoss, idp, AccValTable.oCurGainLoss.ordinal());
                    accountValuesTable.getModel().setValueAt(actPosVal.gainLossLeft, idp, AccValTable.oProfit2Go.ordinal());
                    accountValuesTable.getModel().setValueAt(actPosVal.daysTilExpirey, idp, AccValTable.oDays2Exp.ordinal());
                    if(actPosVal.numOfShares == 0){
                        //select rows that have zero num shares...
                        accountValuesTable.addRowSelectionInterval(idp, idp);
                    }
                }
                System.out.println("\nadded " + listOfPositions.size() + " Positions!");
                System.out.println("\nadded " + posValueList.size() + " posValues!");
                displayValuesRunning = false;
            }
        }
    }
