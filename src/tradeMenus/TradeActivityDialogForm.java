    public void updateTradeActivtyTable(TradeActivityDialogForm taff, int line, TradeRequestData trd){
        
        taff.TradeActivityTable.getModel().setValueAt(Integer.toString(line + 1), line, TblColumns.oNumber.ordinal());
        taff.TradeActivityTable.getModel().setValueAt(trd.getTicker(), line, TblColumns.oTicker.ordinal());
        taff.TradeActivityTable.getModel().setValueAt(trd.getOperationStr(), line, TblColumns.oOperation.ordinal());
        taff.TradeActivityTable.getModel().setValueAt(Integer.toString(trd.getOriginalSharesToTrade()), line, TblColumns.oSharesToTrade.ordinal());
        taff.TradeActivityTable.getModel().setValueAt(Integer.toString(trd.getSharesTraded()), line, TblColumns.oSharesTraded.ordinal());
        taff.TradeActivityTable.getModel().setValueAt(Double.toString(trd.getBid()), line, TblColumns.oBid.ordinal());
        taff.TradeActivityTable.getModel().setValueAt(Double.toString(trd.getAsk()), line, TblColumns.oAsk.ordinal());
        taff.TradeActivityTable.getModel().setValueAt(Double.toString(trd.getLast()), line, TblColumns.oLast.ordinal());
        taff.TradeActivityTable.getModel().setValueAt(Double.toString(trd.getMarket()), line, TblColumns.oMarket.ordinal());
        taff.TradeActivityTable.getModel().setValueAt(Double.toString(trd.getOffer()), line, TblColumns.oOffer.ordinal());
        taff.TradeActivityTable.getModel().setValueAt(Integer.toString(trd.getPriceErrors()), line, TblColumns.oPriceErrs.ordinal());
        taff.TradeActivityTable.getModel().setValueAt(Double.toString(trd.getFilledPrice()), line, TblColumns.oFPrice.ordinal());
        taff.TradeActivityTable.getModel().setValueAt(Double.toString(trd.getMoneySaved()), line, TblColumns.oDollarsSaved.ordinal());
        taff.TradeActivityTable.getModel().setValueAt(Integer.toString(trd.getFillAttempts()), line, TblColumns.oFillAttempts.ordinal());
        taff.TradeActivityTable.getModel().setValueAt(trd.getStatusStr(), line, TblColumns.oStatus.ordinal());
        
        totalMoneySavedLabel.setText(Double.toString(myUtils.roundMe(actTradeReqList.getTotalMoneySaved(), 2)));

    }
    private void clearTradeActivityTable() {
        int rowCount = this.TradeActivityTable.getRowCount();
        int colCount = this.TradeActivityTable.getColumnCount();
        for (int il = 0; il < rowCount; il++) {
            for (int ic = 0; ic < colCount; ic++) {
                this.TradeActivityTable.getModel().setValueAt("", il, ic);
            }
        }
    }
